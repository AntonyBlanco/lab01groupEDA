package Strings;

//Manejo de caracteres especiales
public class SpecialCharacters {
	public static void main(String[] args) {
		System.out.println("CARACTERES ESPECIALES");
		//para insertar, comillas simples, comillas dobles y barra invertida(contra slash)
		System.out.println("\'tú cadena\' ");
		System.out.println("\"tú cadena\"");
		System.out.println("\\tú cadena\\");
		//otros caracteres especiales
		System.out.println("OTROS CARACTERES ESPECIALES");
		System.out.println("Hola \namigos"); //insertar salto de linea 
		System.out.println("Hola\ramigos");
		System.out.println("Hola \t\tamigos");
		System.out.println("Holaa\bmigos");
	}
}
