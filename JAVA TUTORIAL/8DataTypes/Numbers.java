//Tipos de datos Númericos
public class Number {
	public static void main(String[] args) {
		//Datos númericos enteros
		byte num1=100;
		short num2=5000;
		int  num3=1500000;
		long num4=5700000555555555555L;
		//Datos númericos flotantes(reales)
		float num5=0.5f;
		double num6=8.5555d;
		
		System.out.println("Tipos de daios Númericos enteros:");
		System.out.println("byte:  --> Valores entre -128 a 127 / ejemplo: " + num1);
		System.out.println("short: --> Valores entre -32768 hasta 32767 / ejemplo: " + num2);
		System.out.println("int:   --> Valores entre -2147483648 hasta 2147483647 / ejemplo: " + num3);
		System.out.println("long:  --> Valores entre -9223372036854775808 hasta 9223372036854775807 / ejemplo: " + num4);
		System.out.println("-----------------------------------------------------------------------------------------------");
		System.out.println("Tipos de datos Númericos Flotantes:");
		System.out.println("float:  --> Hasta 6 o 7 digitos decimales / ejemplo: " + num5);
		System.out.println("double: -->hasta 15 digitos decimales / ejemplo: " + num6);
	}
}




